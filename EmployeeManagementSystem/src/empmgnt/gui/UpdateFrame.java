    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
       if(validateInput()==false)
        {  JOptionPane.showMessageDialog(null,"please fill all the feilds","Empty Values",JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        try
        {
            Emp e=new Emp();
            int empNo=Integer.parseInt(txtEmpNo.getText());
            String eName=txtEmpName.getText();
            double sal=Double.parseDouble(txtSal.getText());
            e.setEmpno(empNo);
            e.setEname(eName);
            e.setSal(sal);
            if(EmpDao.updateEmployee(e))
            {
                JOptionPane.showMessageDialog(null,"Record updated Sucessfully","Success",JOptionPane.INFORMATION_MESSAGE);
                txtEmpNo.setText("");
                txtEmpName.setText("");
                txtSal.setText("");
                txtEmpNo.requestFocus();
                
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Could not update at database","Error while updating",JOptionPane.ERROR_MESSAGE);
            }  
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in sql!"+ex,"Input Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            
        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"please fill  the  numeric values in EmployeeNo and Sal feilds"+ex,"Input Error",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

